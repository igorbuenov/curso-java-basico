package com.igor.cursojava.aula13;

public class OperadoresRelacionais {
    public static void main(String[] args) {

        int valor1 = 1;
        int valor2 = 2;

        System.out.println("valor1 == valor2: " + (valor1 == valor2));  // Igual
        System.out.println("valor1 != valor2: " + (valor1 != valor2));  // Diferente
        System.out.println("valor1 > valor2: " + (valor1 > valor2));    // Maior
        System.out.println("valor1 >= valor2: " + (valor1 >= valor2));  // Maior ou igual
        System.out.println("valor1 < valor2: " + (valor1 < valor2));    // Menor
        System.out.println("valor1 <= valor2: " + (valor1 <= valor2));  // Meno r ou igual
    }
}
