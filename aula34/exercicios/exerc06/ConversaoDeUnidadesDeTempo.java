package com.igor.cursojava.aula34.exercicios.exerc06;

public class ConversaoDeUnidadesDeTempo {

    public static double minutoParaSegundos(double num) {

        return num * 60;

    }
    public static double horaParaMinutos(double num) {

        return num * 60;

    }

    public static double diaParaHoras(double num) {

        return num * 24;
    }

    public static double semanaParaDias(double num) {

        return num * 7;
    }

    public static double mesParaDias(double num) {

        return num * 30;

    }

    public static double anoParaDias(double num) {

        return num * 365.25;
    }

}
