package com.igor.cursojava.aula34.exercicios.exerc05;

public class ConversaoDeUnidadesDeVolume {


    public static double litroParaCMcubicos(double num) {

        return num * 1000;

    }

    public static double metrosCubicosParaLitros(double num) {

        return num * 1000;

    }

    public static double metrosCubicosParaPesCubicos(double num) {

        return num * 35.32;

    }

    public static double galoesParaPolegadas(double num) {

        return num * 231;

    }

    public static double galoesParalitros(double num) {

        return num * 3.785;

    }

}
