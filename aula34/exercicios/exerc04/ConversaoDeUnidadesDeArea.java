package com.igor.cursojava.aula34.exercicios.exerc04;

public class ConversaoDeUnidadesDeArea {


    public static double metroParaPesQuadrados(double num) {

        return num * 10.76;
    }

    public static double pesQuadradosParaCentimetros(double num) {

        return num * 929;
    }

    public static double milhaParaAcre(double num) {

        return num * 640;
    }

    public static double acreParaPesQuadrados(double num) {

        return num * 43.560;
    }

}
